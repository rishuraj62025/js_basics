// switch

const month = 3
switch (month)
{
    case 1 : 
    console.log(`first month is january`);
    break;
    case 2 : 
    console.log(`second month is february`);
    break;
    case 3 : 
    console.log(`third month is march`);    // this is output
    break;
    case 4 : 
    console.log(`fourth month is april`);
    break;
    case 5 : 
    console.log(`fifth month is may`);
    break;
    default:
        console.log("Default case")
        break;
}

